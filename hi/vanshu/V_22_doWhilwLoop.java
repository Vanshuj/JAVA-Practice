public class V_22_doWhilwLoop {
    public static void main(String[] args) {

        int b = 10;
        do {
            System.out.println(b);
            b++;
        }while(b<5);

        int c = 1;
        do{
            System.out.println(c);
            c++;
        }while(c<=45);
    }
}
